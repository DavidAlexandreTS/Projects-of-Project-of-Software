package iFace;

public class Messages {
	private String msg, sen, rece;

	public void setMsg(String msg) {
		this.msg = msg;
	}

	public void setSen(String name) {
		this.sen = name;
	}

	public void setRece(String namefriend) {
		this.rece = namefriend;
	}

	public String getMsg() {
		return msg;
	}

	public String getSen() {
		return sen;
	}

	public String getRece() {
		return rece;
	}
}
